function [colors]=makecolor(brightness,fig)
%this function creates an assortment of 18 RGB colors.  You can
%brightness is a gradient of brightness 0 is normal, -1 is black, 1 is white,  
%the dimension of the RGB colors  ex. 2 generates a 18x3 array while 3
%generates a 13x1x3 array.
%fig shows visualization of the output (true,false)
if nargin<1
    brightness = 0;
end

if nargin<2
    fig=false;
end


colors=[0.9     0        0; ...
        0       0.9      0; ...
        0       0        1; ...
        0.9     0.6      0; ...
        0.5     0        0.5; ...
        0       0.8      0.6; ...
        0.66    0.3      0; ...
        0.4     0.66     0; ...
        0       0.3      0.66; ...
        0       0.66     0.3; ...
        0.3     0        0.66; ...
        0.66    0        0.3; ...
        0.55    0.22     0.22; ...
        0.22    0.55     0.22; ...
        0.22    0.22     0.55; ...
        0.4     0.4      0.2; ...
        0.2     0.4      0.4; ...
        0.4     0.2      0.4];           
if brightness>=0
    colors = colors + (1 - colors)*brightness;
else
    colors=colors*(1 - abs(brightness));
end

if fig
    figure
    imshow(repelem(permute(colors,[1,3,2]),20,20,1))
end

end